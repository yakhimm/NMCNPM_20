// show/hide like/dislike button in detail recipe layout
$("#favoriteButton").on('click', function () {

    if ($('#favoriteButton > ion-icon').attr('name') === "heart-outline") {
        $('#favoriteButton > ion-icon').attr('name', 'heart-dislike-outline');
    }
    else {
        $('#favoriteButton > ion-icon').attr('name', 'heart-outline');
    }
})
